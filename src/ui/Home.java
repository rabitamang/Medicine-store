    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        String name= txtMname.getText();
        String price= txtMprice.getText();
        String quantity= txtMQty.getText();
        String expiry= dateExpiry.getDate().toString();
        //to check if the medicine is already registered
        String check_sql="SELECT * FROM medicine WHERE name='"+name+"'";
        try {
            ps= (PreparedStatement)con.prepareStatement(check_sql);
           rs= ps.executeQuery();
        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            if (!rs.isBeforeFirst()) {
                //to insert record as no data with the same name is found
                String sql= "INSERT INTO medicine(name,price,stock,expiry_date) VALUES('"+name+"','"+price+"','"+quantity+"','"+expiry+"')";
                try {
                    //System.out.println(sql);
                    ps= (PreparedStatement)con.prepareStatement(sql);
                    ps.executeUpdate();
                    txtMname.setText("");
                    txtMprice.setText("");
                    txtMQty.setText("");
                    lblInfo.setText("New Medicine Inserted");
                } catch (SQLException ex) {
                    Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }
            else{
               lblInfo.setText("This Medicine is already registered.");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
        
       
    }//GEN-LAST:event_btnInsertActionPerformed

