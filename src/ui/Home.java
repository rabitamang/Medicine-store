    private void view_table(int button_number) {
        //10 is to set how much record is displayed in a page
        int lower_limit=15*(button_number-1);
               
        try {
            String sql_table = "SELECT id,name,price,stock,expiry_date FROM medicine ORDER BY name LIMIT "+lower_limit+",15";
            //System.out.println(sql_table);System.exit(0);
            ps = (PreparedStatement) con.prepareStatement(sql_table);
            rs = ps.executeQuery();
            tblInventory.setModel(DbUtils.resultSetToTableModel(rs));
    
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    public void pagination(){
        //make the pagination button invisible at first
        btn_page1.setVisible(false);
        btn_page2.setVisible(false);
        btn_page3.setVisible(false);
        btn_page4.setVisible(false);
        btn_page5.setVisible(false);
       
         try {
            String sql_table = "SELECT * FROM medicine";
            //System.out.println(sql_table);System.exit(0);
            ps = (PreparedStatement) con.prepareStatement(sql_table);
            rs = ps.executeQuery(); 
            //to count the total number of rows
            rs.last();
            int number_of_rows=rs.getRow();
            int page_no=number_of_rows/15+1;
            //to set label info like 1 out of 5 pages
          /*ahile max 5 pages matra support garchha, 5*15= 75 items, dherai chaiyama 
            item per page(15) lai increase garne ani switch case badhaune like case 6 garne
            plus naya button pani banaune*/
            lblInfo2.setText(""+page_no);
            switch (page_no) {
                case 5:
                    btn_page1.setVisible(true);
                    btn_page2.setVisible(true);
                    btn_page3.setVisible(true);
                    btn_page4.setVisible(true);
                    btn_page5.setVisible(true);
                    
                    break;
                case 4:
                    btn_page1.setVisible(true);
                    btn_page2.setVisible(true);
                    btn_page3.setVisible(true);
                    btn_page4.setVisible(true);
                   
                    break;
                case 3:
                    btn_page1.setVisible(true);
                    btn_page2.setVisible(true);
                    btn_page3.setVisible(true);
                   
                    break;
                case 2:
                    btn_page1.setVisible(true);
                    btn_page2.setVisible(true);
                   
                    break;
                default:
                    btn_page1.setVisible(true);
                   
                    break;
            }
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        
    }
