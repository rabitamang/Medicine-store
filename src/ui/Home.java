public Float tempConverter(Float value,String from,String to){
            Float result = null;
    if (null != from) switch (from) {
            case "Celsius":
                if (null != to) switch (to) {
                    case "Fahrenheit":
                        result = (float)(value * 1.8 + 32);
                        break;
                    case "Kelvin":
                        result = (float)(value + 273);
                        break;
                    case "Celsius":
                        result=value;
                        break;
                    default:
                        break;
                }   break;
            case "Fahrenheit":
                if (null != to) switch (to) {
                    case "Celsius":
                        result = (float)((value -32)*100/180);
                        break;
                    case "Kelvin":
                        result = (float)((value -32)*100/180 +273);
                        break;
                    case "Fahrenheit":
                        result=value;
                        break;
                    default:
                        break;
                }   break;
            case "Kelvin":
                if (null != to) switch (to) {
                    case "Celsius":
                        result = (float)((value -273));
                        break;
                    case "Fahrenheit":
                        result = (float)((value -273)*180/100+32);
                        break;
                    case "Kelvin":
                        result=value;
                        break;
                    default:
                        break;
                }   break;
            default:
                break;
        }
    return result;
}
